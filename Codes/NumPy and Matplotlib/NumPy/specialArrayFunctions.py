import numpy as np

# NumPy function for creating arrays with all zeros
a = np.zeros(5) 
print("a: \n", a)
b = np.zeros((3, 3))
print("\nb: \n", b)

# NumPy function for creating arrays with all ones
c = np.ones(10) 
print("c: \n", c)
d = np.ones((3, 3, 2))
print("\nd: \n", d)


# NumPy function for creating identity matrix
i = np.identity(4) 
print("i: \n", i)